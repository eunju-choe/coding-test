## 내 답안
def charge(n):
    # 동전의 개수
    ret = 0
    
    # 500원
    n500 = n // 500
    ret += n500
    n -= 500*n500

    # 100원
    n100 = n // 100
    ret += n100
    n -= 100*n100

    # 50원
    n50 = n//50
    ret += n50
    n -= 50*n50

    n10 = n//10
    ret += n10
    n -= 10*n10

    return ret

print(charge(1260))


